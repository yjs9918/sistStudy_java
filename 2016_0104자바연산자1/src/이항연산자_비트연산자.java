/*
 *   비트연산자 ( & , | , ^) : 논리회로
 *      & => 직렬
 *      | => 병렬
 *   ==========================
 *          &(*)   |(+)   ^(같은거일때 0 다른거일때 1)
 *   ==========================
 *     00    0      0     0
 *   ==========================
 *     01    0      1     1
 *   ==========================
 *     10    0      1     1
 *   ==========================
 *     11    1      1     0
 *   ==========================
 *   
 *     10 & 6
 *     1010
 *     0110 &
 *     ======
 *     0010  => 2
 *     
 *     23 & 8
 *     10111
 *     01000 &
 *     =======
 *     00000 => 0
 *     
 *     10 | 6
 *     1010
 *     0110 |
 *     ======
 *     1110  => 14
 *     
 *     23 | 8
 *     10111
 *     01000 |
 *     =======
 *     11111 => 31
 *     
 *     10 ^ 6
 *     1010
 *     0110 ^
 *     ======
 *     1100  => 12
 *     
 *     23 ^ 8
 *     10111
 *     01000 ^
 *     =======
 *     11111 => 31
 */
public class 이항연산자_비트연산자 {

	public static void main(String[] args) {
		// TODO Auto-generated method stub
		System.out.println("10&6="+(10&6));
		System.out.println("23&8="+(23&8));
		
		System.out.println("10|6="+(10|6));
		System.out.println("23|8="+(23|8));
		
		System.out.println("10^6="+(10^6));
		System.out.println("23^8="+(23^8));

	}

}
